# ---------------- KEYBOARD & LIVE UI ----------------
def on_press(key):
    try:
        if key == keyboard.Key.right: engine.view_date += timedelta(days=1)
        elif key == keyboard.Key.left: engine.view_date -= timedelta(days=1)
        elif hasattr(key, 'char') and key.char == 'p':
            res = engine.get_analysis(for_pdf=True)
            fname = generate_report_pdf(engine.name, engine.dob, engine.tob, f"Rasi {engine.natal_moon_rasi+1}", res, engine.view_date.strftime('%d-%b-%Y'))
            os.system(f"start {fname}" if sys.platform == "win32" else f"open {fname}")
    except: pass

keyboard.Listener(on_press=on_press).start()

